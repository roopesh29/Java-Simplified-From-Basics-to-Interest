/*Understanding if Statements in Java"
The if statement is a fundamental control structure in Java used to execute code based on a condition. It helps make decisions in a program.
 *
 */
class IfExample {
    public static void main(String[] args) {
        int number = 10; // Example number

        if (number > 0) {
            System.out.println("The number is positive.");
        }

        System.out.println("Program complete.");

    }
}
